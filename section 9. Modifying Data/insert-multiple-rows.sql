-- la declaracionn insert para insertar multiples filas sigue las mismas reglas que la de insert para una sola fila, solo que la lista de valores se separa por comas
-- ejemplo:
-- la declaracion INSERT tiene la siguiente estructura:
INSERT INTO
  table_name (column1, column2, column3)
VALUES
  (value1, value2, value3),
  (value1, value2, value3),
  (value1, value2, value3);